class WholeNumber(object):
    def __init__(self,a=0,b=0):
        self.a = a
        self.b = b
    
    def mult(self):
        if type(self.a) != int or type(self.b) != int:
            return None
        elif self.a < 0 or self.b < 0:
            return None
        else:
            return self.a * self.b
  
    def add(self):
        if type(self.a) != int or type(self.b) != int:
            return None
        elif self.a < 0 or self.b < 0:
            return None
        else:
            return self.a + self.b

    def sub(self):
        if type(self.a) != int or type(self.b) != int:
            return None
        elif self.a < 0 or self.b < 0:
            return None
        elif self.a < self.b:
            return None
        else:
            return self.a - self.b
